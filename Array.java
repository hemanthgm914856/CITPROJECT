public class Array {
    public static void main(String[] args) {
        for (int i = 0; i < 2; i++) {
            System.out.println("Tiger");
            for (int j = 0; j < 2; j++) {
                System.out.println("    Lion");
            }
            for (int k = 0; k < 3 ; k++) {
                System.out.println("Cat");
            }
        }
    }
}
